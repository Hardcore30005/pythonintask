import random

words = ["Дуэль","Плакат", "Гитара","Колесо","Книга","Слово","Сталь","Интрига","Цветок","Дерево"]
ranWord = random.choice(words) 
length = len(ranWord)
attempt = 5

print("Комьютер загадал слово. В слове " + str(length) + " букв")
while attempt > 0:
	letter = input("Вы можете узнать если ли какая-нибудь буква в слове, чтобы после угадать его! ")
	if letter in list(ranWord): 
		print("Буква " + letter + " есть в слове")
	else:
		print("Буквы " + letter + " нет в слове")
	attempt -=1

userWord = input("Попробуйте угадать слово ")	
if userWord==ranWord:
	print("Вы молодец!")
else:
	print("К сожалению, нет")
	
input()